interface LanguageProviderProps {
  children: ReactNode;
  urlLang?: Language;
}

export function LanguageProvider({ children, urlLang }: LanguageProviderProps) {
  const [language, setLanguageState] = useState<Language>(() => {
    if (urlLang) return urlLang;
    const saved = localStorage.getItem('language');
    if (saved && (saved === 'en' || saved === 'ru' || saved === 'ua')) {
      return saved;
    }
    const browserLang = navigator.language.toLowerCase();
    if (browserLang.startsWith('ru')) return 'ru';
    if (browserLang.startsWith('uk')) return 'ua';
    return 'en';
  });

  // Sync language when URL changes
  useEffect(() => {
    if (urlLang && urlLang !== language) {
      setLanguageState(urlLang);
    }
  }, [urlLang]);

  useEffect(() => {
    localStorage.setItem('language', language);
  }, [language]);

  const navigate = useNavigate();
  const location = useLocation();

  const setLanguage = useCallback((newLang: Language) => {
    setLanguageState(newLang);
    localStorage.setItem('language', newLang);
    
    const urlPrefix = langToUrl(newLang);
    const pathMatch = location.pathname.match(/^\/(en|ru|uk)(\/.*)?$/);
    const subPath = pathMatch ? (pathMatch[2] || '') : '';
    navigate(`/${urlPrefix}${subPath}`);
  }, [navigate, location]);

  const localePath = useCallback((path: string) => {
    const prefix = langToUrl(language);
    if (path === '/') return `/${prefix}`;
    return `/${prefix}${path}`;
  }, [language]);

  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, localePath }}>
      {children}
    </LanguageContext.Provider>
  );
}

/** Fallback used during HMR / initial mount race conditions */
const fallbackContext: LanguageContextType = {
  language: 'en',
  setLanguage: () => {},
  t: (key: string) => key,
  localePath: (path: string) => `/en${path === '/' ? '' : path}`,
};

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    // During HMR or direct-load race conditions the provider may not yet be mounted.
    // Return a safe fallback instead of crashing — the real provider will re-render immediately.
    if (import.meta.hot) {
      return fallbackContext;
    }
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
